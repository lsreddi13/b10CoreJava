package com.Day3;

public class DemoLoops {

	// print 1 to 10 numbers.
	public static void main(String[] args) {

		// 0 1 2..10

		// print prime number

//		ctrl + shift + /
//		
//		ctrl + shift + \
		for (int i = 10; i >= 10; i--) {
			System.out.println(i);
		}

		// print 50 to 60 numbes

		for (int i = 50; i <= 60; i++) {
			System.out.println(i);
		}

	}

}
